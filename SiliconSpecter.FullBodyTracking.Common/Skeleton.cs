namespace SiliconSpecter.FullBodyTracking.Common
{
  /// <summary>
  /// A skeleton of <see cref="Bone"/>s.
  /// </summary>
  public struct Skeleton
  {
    /// <summary>
    /// The hip <see cref="Bone"/>.
    /// </summary>
    public Bone Hips;

    /// <summary>
    /// The shoulder <see cref="Bone"/>.
    /// </summary>
    public Bone Shoulders;

    /// <summary>
    /// The head <see cref="Bone"/>.
    /// </summary>
    public Bone Head;

    /// <summary>
    /// The left upper arm <see cref="Bone"/>.
    /// </summary>
    public Bone LeftUpperArm;

    /// <summary>
    /// The left lower arm <see cref="Bone"/>.
    /// </summary>
    public Bone LeftLowerArm;

    /// <summary>
    /// The left hand <see cref="Bone"/>.
    /// </summary>
    public Bone LeftHand;

    /// <summary>
    /// The right upper arm <see cref="Bone"/>.
    /// </summary>
    public Bone RightUpperArm;

    /// <summary>
    /// The right lower arm <see cref="Bone"/>.
    /// </summary>
    public Bone RightLowerArm;

    /// <summary>
    /// The right hand <see cref="Bone"/>.
    /// </summary>
    public Bone RightHand;

    /// <summary>
    /// The left upper leg <see cref="Bone"/>.
    /// </summary>
    public Bone LeftUpperLeg;

    /// <summary>
    /// The left lower leg <see cref="Bone"/>.
    /// </summary>
    public Bone LeftLowerLeg;

    /// <summary>
    /// The left foot <see cref="Bone"/>.
    /// </summary>
    public Bone LeftFoot;

    /// <summary>
    /// The left toe <see cref="Bone"/>.
    /// </summary>
    public Bone LeftToes;

    /// <summary>
    /// The Right upper leg <see cref="Bone"/>.
    /// </summary>
    public Bone RightUpperLeg;

    /// <summary>
    /// The Right lower leg <see cref="Bone"/>.
    /// </summary>
    public Bone RightLowerLeg;

    /// <summary>
    /// The Right foot <see cref="Bone"/>.
    /// </summary>
    public Bone RightFoot;

    /// <summary>
    /// The Right toe <see cref="Bone"/>.
    /// </summary>
    public Bone RightToes;
  }
}
